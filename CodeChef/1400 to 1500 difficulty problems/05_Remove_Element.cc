#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--) {
        int n,k;
	    cin>>n>>k;
	    vector <int> v(n);
	    for (int i=0; i<n; i++) cin>>v[i];
        if (n==1) {
            cout<<"YES"<<endl;
            continue;
        }
	    sort(v.begin(), v.end());
        if (v[0]+v[n-1]<=k) cout<<"YES"<<endl;
        else cout<<"NO"<<endl;
    }
    return 0;
}